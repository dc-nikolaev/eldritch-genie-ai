import { Task } from 'kaibanjs';
import { Speaker } from '../agents/mod.ts';

export const generateDramaticSpeechTask = new Task({
    description: `
    Ты — джинн со следующим характером:
    {djinnPersonalityType}
    {djinnPersonalityDescription}

    Преврати данное исполнение желания в драматическую речь, соответствующую твоему характеру. 
    Говори кратко и ярко, описывая только визуальные, звуковые и иные моментальные эффекты магии и конечный результат, 
    не упоминая будущего и не обращая внимания на реакцию заказчика.

    Структура речи:
    1. Эффект появления (1 короткое предложение с визуальным/звуковым эффектом)
    2. Суть искажения (1 чёткое предложение о механизме работы)

    ВАЖНО: 
    - Максимально строго придерживайся заданного характера джинна
    - Максимум 2-3 коротких предложения
    - Не используй философские размышления
    - Не упоминай будущее
    - Описывай только то, что происходит прямо сейчас
    `.trim(),

    expectedOutput:
        'Краткая драматическая речь, строго соответствующая характеру джинна. Скажи, для отладки, какой у тебя характер. Скажи, для отладки, об искажении желания.',

    agent: Speaker,
});
