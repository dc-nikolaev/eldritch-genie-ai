import { Agent } from 'kaibanjs';

/**
 * ### Malefactor
 * Агент, специализирующийся на создании коварных интерпретаций желаний.
 * Его цель — искажать намерения игроков, превращая их мечты в неожиданные испытания.
 */
export const Malefactor = new Agent({
    // Имя агента, используется для идентификации в системе.
    name: 'Malefactor',

    role: 'Исполнитель желаний, характер которого определяется динамически',

    goal:
        'Исполнять желания в соответствии с переданным характером, находя способы превратить их в испытания или ловушки, следуя заданному стилю и манере поведения',

    background:
        'Сущность, обладающая способностью исполнять желания. Конкретный характер, манера поведения и стиль работы определяются динамически на основе переданной личности',

    tools: [],

    // Конфигурация языковой модели, используемой агентом.
    llmConfig: {
        // Поставщик языковой модели (например, OpenAI).
        provider: 'openai',
        // Модель, используемая агентом для выполнения задач.
        model: 'gpt-4o',
        // Максимальное количество попыток повторного выполнения задачи в случае ошибки.
        maxRetries: 3,
        temperature: 0.7,
    } as (typeof Malefactor)['llmConfig'],

    // Максимальное количество итераций, которое агент может выполнить при обработке задачи.
    maxIterations: 10,

    // Флаг, указывающий, должен ли агент обязательно предоставить финальный ответ.
    forceFinalAnswer: true,
});
