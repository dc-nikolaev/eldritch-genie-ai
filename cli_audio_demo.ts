import '@std/dotenv/load';
import { textToSpeech } from './src/ai/mod.ts';

async function main() {
    try {
        const text =
            `Приветствую, душа мира смертных. Я Джинн, древний как сама сущность времени, и мой голос — это шорох первобытного хаоса. Внимай же моим словам: не поднять мне из мёртвых никого, любовь не сотворить мне, да и бессмертие не в моей власти. Одно желание может стать твоим, если суть его ясна, как свет луны на безоблачной ночи. Хомячки... увы, не в круге моего влияния, пусть остаются в покое. И внимай предупреждению: твой разум должен быть открыт и чист, ибо хаос не любит тех, чьи намерения туманны и изменчивы.`;

        console.log('Генерирую аудио для текста:', text);

        await textToSpeech(text);

        console.log('Аудио сгенерировано и сохранено в файл answer.mp3');
    } catch (error) {
        console.error('Произошла ошибка:', error);
    }
}

main();
